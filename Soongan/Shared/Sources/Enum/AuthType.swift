import Foundation

/// `AuthType` 열거형은 사용자의 인증 상태를 표현하기 위한 타입입니다.
/// 이 타입은 로그인 흐름의 상태 관리 및 화면 전환 로직에서 핵심적으로 사용됩니다.
///
/// 인증 상태는 총 세 가지로 구성되며, 각각의 상태는 앱 내에서 특정한 의미와 UI 흐름에 영향을 미칩니다.
///
/// ## 열거형의 각 케이스
/// - `loggedIn`: 사용자가 정상적으로 로그인된 상태를 나타냅니다.
///              이 상태에서는 보통 메인 화면 또는 인증된 사용자만 접근 가능한 기능을 사용할 수 있습니다.
///
/// - `loggedOut`: 사용자가 로그아웃된 상태이며, 로그인 화면이 기본으로 표시됩니다.
///               최초 앱 실행 시 기본값으로 설정되며, 사용자가 로그인하지 않은 일반적인 상태입니다.
///
/// - `skipped`: 사용자가 로그인하지 않고 앱을 둘러보기 위해 '둘러보기' 옵션을 선택한 상태입니다.
///             제한된 기능만 사용할 수 있습니다.
///
/// ## 사용 예시
/// - `@Shared(.appStorage("AuthState")) var authState: AuthType = .loggedOut`
///   → 앱 전체에서 인증 상태를 저장하고, 상태 변화에 따라 UI를 자동으로 업데이트합니다.
///
/// ## 주의사항
/// - 이 열거형은  `@Shared`와 함께 사용되므로 `RawRepresentable`을 만족하기 위해 `String` 타입을 사용합니다.
/// - `Equatable`을 채택하여 상태 비교가 가능합니다.
///
/// - Note: `AuthType`은 단순 상태 표현 이상의 의미를 가지며,
///          상태에 따라 다른 View를 렌더링하거나 라우팅 조건으로 활용됩니다.
public enum AuthType: String, Equatable {
    
    /// 로그인하지 않고 앱 사용을 선택한 상태입니다.
    case skipped
    
    /// 정상적으로 로그인된 상태입니다.
    case loggedIn
    
    /// 로그아웃되었거나 로그인하지 않은 상태입니다.
    case loggedOut
}
